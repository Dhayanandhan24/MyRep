package Lab01;

public class Account {

	private int accId;
	private String accName;
	
	public Account(int accId, String accName) {
		super();
		this.accId = accId;
		this.accName = accName;
	}
	public int getAccId() {
		return accId;
	}

	public void setAccId(int accId) {
		this.accId = accId;
	}

	public String getAccName() {
		return accName;
	}

	public void setAccName(String accName) {
		this.accName = accName;
	}
	}
	

